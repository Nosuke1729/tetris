// ミノのセル座標リストを返す (盤面絶対座標)
export function getPieceCells(
  type: PieceType,
  x: number,
  y: number,
  rotation: Rotation
): { x: number; y: number }[] {
  const shape = PIECE_SHAPES[type][rotation];
  const cells: { x: number; y: number }[] = [];
  for (let row = 0; row < shape.length; row++) {
    for (let col = 0; col < shape[row].length; col++) {
      if (shape[row][col]) {
        cells.push({ x: x + col, y: y + row });
      }
    }
  }
  return cells;
}

// ミノのスポーン位置
export function getSpawnPosition(type: PieceType): { x: number; y: number } {
  if (type === "I") return { x: 3, y: 0 };
  if (type === "O") return { x: 3, y: 0 };
  return { x: 3, y: 0 };
}
